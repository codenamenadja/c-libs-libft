int     ft_isspace(int c)
{
    return (
            (
             (c == '\f') ||
             (c == '\n') ||
             (c == '\r') ||
             (c == '\t') ||
             (c == '\v') ||
             (c == ' ')
            ) ? 
            1 : 0
            );
}

