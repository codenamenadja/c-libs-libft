#include <unistd.h>

void        ft_bzero(void *s, size_t n)
{
    char    *s_pt;
    size_t  i;

    i = 0;
    s_pt = s;
    while (i < n && *(s_pt + i))
        *(s_pt+i++) = 0;
}
